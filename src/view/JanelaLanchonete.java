    public Lanchonete CarregaLanchonete() {
        //faz a leitura dos valores da janela em um objeto lanchonete
        Lanchonete lan = null;         
        String cnpj = txCnpj.getText();
        String nome = txNome.getText();
        int numerofunc = Integer.valueOf(txNumerofunc.getText());
        lan = new Lanchonete(cnpj, nome,numerofunc);
        return lan;

    }
    public void ExibeRegistro(Lanchonete lan) {
        //faz a leitura do objeto cliente e atribui os valores lidos aos objetos visuais (Textfields)
        try{
        txNome.setText(lan.getNome());
        txNumerofunc.setText(String.valueOf(lan.getNumerofunc()));
        }catch (NullPointerException erro){
            System.out.println(erro);
        }
    }      

