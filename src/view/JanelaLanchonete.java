    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
       try{
        Lanchonete lan = CarregaLanchonete();
        if (dao.Registrar(lan)) {
            JOptionPane.showMessageDialog(this, "Lanchonete inserido com sucesso.");
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao inserir Lanchonete.");
        }
       }catch(NumberFormatException error){
                JOptionPane.showMessageDialog(null,
                "Todos os campos precisam ser preenchidos e numero de funcionarios deve ser um numero inteiro",
                "Erro na execução da inserção",
                JOptionPane.ERROR_MESSAGE);                                   
            System.out.println("Erro na execução da inserção = " + error);
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed
    
    
