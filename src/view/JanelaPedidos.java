    public Pedido CarregaPedido() {
        //faz a leitura dos valores da janela em um objeto Cliente
        Pedido ped = null;

            int id = Integer.valueOf(txId.getText());
            String lanche = txLanche.getText();
            String adicional = txAdicional.getText();
            float preco = Float.parseFloat(txPreco.getText());
            ped = new Pedido(id, lanche,adicional , preco);
            return ped;

    }
    public void ExibeRegistro(Pedido ped) {
        //faz a leitura do objeto cliente e atribui os valores lidos aos objetos visuais (Textfields)
        try{
        txLanche.setText(ped.getLanche());
        txAdicional.setText(ped.getAdicional());
        txPreco.setText(String.valueOf(ped.getPreco()));
        }catch (NullPointerException erro){
            System.out.println(erro);
        }
    }      

