    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
       try{
        Pedido ped = CarregaPedido();
        if (dao.Registrar(ped)) {
            JOptionPane.showMessageDialog(this, "Pedido inserido com sucesso.");
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao inserir Pedido.");
        }
       }catch(NumberFormatException error){
                JOptionPane.showMessageDialog(null,
                "Todos os campos de cadastro precisam ser preenchidos, id deve ser um numero inteiro e preco deve ser um numero",
                "Erro na execução da inserção",
                JOptionPane.ERROR_MESSAGE);                                   
            System.out.println("Erro na execução da inserção = " + error);
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed
    
    
